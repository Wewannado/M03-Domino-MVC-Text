    private void jRBJugActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRBJugActionPerformed
        if (evt.getActionCommand().equals("2Jugadors")) {
            Logger.getLogger(getClass().getName()).log(
                    Level.CONFIG, "2 jugadors", err);
            etJugador3.setVisible(false);
            etJugador4.setVisible(false);
            jLabelJugador3.setVisible(false);
            jLabelJugador4.setVisible(false);
        }
        if (evt.getActionCommand().equals("4Jugadors")) {
            Logger.getLogger(getClass().getName()).log(
                    Level.CONFIG, "4 jugadors", err);
            etJugador3.setVisible(true);
            etJugador4.setVisible(true);
            jLabelJugador3.setVisible(true);
            jLabelJugador4.setVisible(true);

        }
    }//GEN-LAST:event_jRBJugActionPerformed

    private void jCBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCBActionPerformed

        if ("IA".equals(evt.getActionCommand())) {
            System.out.println("Checkbox IA pulsat");
            if (jCBIa.isSelected() == false && jCBVeureFitxes.isSelected() == false) {
                JOptionPane.showMessageDialog(this, "IA desactivada, controlaras a todos los jugadores");
                this.jCBVeureFitxes.setSelected(true);
            }
        }
        if ("veureFitxes".equals(evt.getActionCommand())) {
            System.out.println("Checkbox Veure Fitxes pulsat");
            if (jCBIa.isSelected() == false) {
                JOptionPane.showMessageDialog(this, "IA desactivada, por lo que debes ver las fichas de todos los jugadores.");
                this.jCBVeureFitxes.setSelected(true);
            }
        }
    }//GEN-LAST:event_jCBActionPerformed

