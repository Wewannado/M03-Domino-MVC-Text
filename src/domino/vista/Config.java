    private void btJugarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btJugarActionPerformed
        boolean ok = false;
        if (jRB4Jug.isSelected()) {
            if (("".equals(etJugador1.getText())) || "".equals(etJugador2.getText()) || "".equals(etJugador3.getText()) || "".equals(etJugador4.getText())) {
                JOptionPane.showMessageDialog(this, "Es obligatori omplir el nom dels jugadors");
            } else {
                ok = true;
            }
        } else {
            if ("".equals(etJugador1.getText()) || "".equals(etJugador2.getText())) {
                JOptionPane.showMessageDialog(this, "Es obligatori omplir el nom dels jugadors");
            } else {
                ok = true;
            }
        }
        if (ok) {
            this.setVisible(false);
        }
    }//GEN-LAST:event_btJugarActionPerformed

